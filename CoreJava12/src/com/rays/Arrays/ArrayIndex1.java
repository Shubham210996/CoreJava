package com.rays.Arrays;

public class ArrayIndex1 {
	public static void main (String[] args)
	{
		int[] User = new int [5];
		User[0] = 2;
		User[1] = 4;
		User[2] = 6;
		User[3] = 8;
		User[4] = 10;
		
		System.out.println("Value at index 0 is = "+User[0]);
		System.out.println("Value at index 1 is = "+User[1]);
		System.out.println("Value at index 2 is = "+User[2]);
		System.out.println("Value at index 3 is = "+User[3]);
		System.out.println("Value at index 4 is = "+User[4]);
	}

}
