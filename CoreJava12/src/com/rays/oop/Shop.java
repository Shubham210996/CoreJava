package com.rays.oop;

public class Shop {
	private String color = null;
	private int borderWidth = 0;
	
	public void setColor(String c) {
		color = c;
	}
	public String getColor() {
		return color;
	}
	public void setBorderWidth(int bw) {
		borderWidth = bw;
	}
	public int getBorderWidth() {
		return borderWidth;
	}
}
